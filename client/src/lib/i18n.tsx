interface I18nContextType {
  language: SupportedLanguage;
  setLanguage: (lang: SupportedLanguage) => void;
  t: (key: string, fallback?: string) => string;
  isRTL: boolean;
  dir: 'ltr' | 'rtl';
}

const I18nContext = createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<SupportedLanguage>(() => {
    if (typeof window !== 'undefined') {
      const stored = localStorage.getItem('language') as SupportedLanguage;
      if (stored && SUPPORTED_LANGUAGES.includes(stored)) {
        return stored;
      }
      const pathLang = window.location.pathname.split('/')[1] as SupportedLanguage;
      if (SUPPORTED_LANGUAGES.includes(pathLang)) {
        return pathLang;
      }
    }
    return 'en';
  });

  const isRTL = RTL_LANGUAGES.includes(language);
  const dir = isRTL ? 'rtl' : 'ltr';

  const setLanguage = useCallback((lang: SupportedLanguage) => {
    setLanguageState(lang);
    localStorage.setItem('language', lang);
  }, []);

  const t = useCallback((key: string, fallback?: string): string => {
    const translations = defaultTranslations[language] || defaultTranslations.en;
    return translations[key] || defaultTranslations.en[key] || fallback || key;
  }, [language]);

  useEffect(() => {
    document.documentElement.dir = dir;
    document.documentElement.lang = language;
    if (isRTL) {
      document.body.classList.add('rtl');
      document.body.style.fontFamily = "'Noto Sans Arabic', sans-serif";
    } else {
      document.body.classList.remove('rtl');
      document.body.style.fontFamily = "'Inter', sans-serif";
    }
  }, [language, dir, isRTL]);

  return (
    <I18nContext.Provider value={{ language, setLanguage, t, isRTL, dir }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return context;
}
